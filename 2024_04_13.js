function rotateClockwise(matrix) {
    /*********************************************************
      ____                 _    ____          _ _             
     / ___| ___   ___   __| |  / ___|___   __| (_)_ __   __ _ 
    | |  _ / _ \ / _ \ / _` | | |   / _ \ / _` | | '_ \ / _` |
    | |_| | (_) | (_) | (_| | | |___ (_) | (_| | | | | | (_| |
     \____|\___/ \___/ \__,_|  \____\___/ \__,_|_|_| |_|\__, |
                                                        |___/ 
    *********************************************************/
    /*********************************************************
     _____ _                  _                           _     
    |_   _| |___  ___ _ _ __ | | __   _   _  ___  _   _  | |                
      | | |  _  \/ _ | | `_ \| |/ /  | | | |/ _ \| | | | | |                       
      | | | | | | (_)  | | | |   /   |  \| | (_) | |_| | |_|                    
      |_| | | |_|\___|_|_| |_|_|_\    \_   |\___/\___`_|  _ 
                                      |___/              |_|
    *********************************************************/

    if (matrix.join("").length === 0) return [];
    let nm = new Array(matrix[0].length).fill("").map(e => new Array(matrix.length).fill("").slice());
    for (let i = 0; i < nm.length; i++) {
        for (let j = 0; j < nm[i].length; j++) {
            nm[i][j] = matrix[matrix.length - j - 1][i];
        }
    }
    return nm.map(r => r.join(""));
}